class Solution
{
  public:
    int minOperation(int n)
    {
        //code here
        int cnt=0;
        int a=n;
        while(a){
            if(a%2==0)a=a/2;
            else{
                a=a-1;
            }
            cnt++;
        }
        return cnt;
    }
};

