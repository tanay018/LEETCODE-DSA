class Solution{
	
	
	
	public:
	void shuffleArray(int arr[],int n)
	{
	    vector<int>temp(arr,arr+n);
        int j=n/2,k=0;
        for(int i=0;i<n;i+=2){
            
            arr[i]=temp[k++];
            arr[i+1]=temp[j++];
        }
	}		 

};

